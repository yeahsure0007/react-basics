import './index.css';
import App from './App';
import reportWebVitals from './reportWebVitals';
import axios from 'axios';

axios.interceptors.request.use(request => {
  // Any edits before sending it to server.
  console.log(request);
  return request;
}, error => {
  console.log(error);
  return Promise.reject(error);
});

